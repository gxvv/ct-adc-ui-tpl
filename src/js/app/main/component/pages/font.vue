### 常用格式

常用格式定义了日常的信息展示方式

### 统一交互

#### 日期

**分隔符:** '-'

**小时制:** 24小时制

#### 状态类

**可用状态:** 如已启用、已生效、已通过

**不可用状态:** 如已禁用、未生效、已驳回

**挂起状态:** 如测试中、待领取

#### 并列信息(id/name同时展示)

分隔符: ' | '

**两个信息:** 对于两个信息并列的情况，采用id | name

**多信息** 对于多个信息并列的情况，采用id | code | name

