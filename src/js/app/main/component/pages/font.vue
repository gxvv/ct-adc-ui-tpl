### 释义

文本包含系统中所有的文本展示

### 范围

包含但不限于：系统数据展示、用户引导提示、错误提示、信息显示辅助说明(如标题)、按钮文字等

### 整体文案风格

**简洁:** 内容简洁，字数尽量少，避免冗余

**易懂:** 文字易理解，拒绝个性化或不统一

**颜色:** 文字的颜色和其意义符合，需遵循`设计风格-颜色`

